    <h2 class="text-center">How I was fired from my own startup</h2>
    <br>
    <p class="post">
    How naive I was when we fundraised money.
    </p>
    <p class="post">I had a good idea and all the strategy in my head.<br>
    But I wanted to recruit the best talents, to build the best product, with the best User eXperience. And for that, I needed money.<br>
    My project was one of the sexiest on the market at this time. All the investment funds were calling us to have a chance to be part of our first round. We were like the most beautiful girl of the party, the one who can choose any boy she wants. So we chose the most famous ones.
    </p>
    <p class="post">
    They gave us a large amount of money. More than I could have ever dream. Our financial valuation was insane. I became instantly millionaire for just an idea, just a project. Nothing real.<br>
    In return, they get a minor part of the company’s shares. That’s how it works. They are here to invest. And like all investors, they want to measure the risk. So, they were now part of the Board.<br>
    </p>
    <p class="post">
    At this precise moment, I understood Ornikar was not my company anymore.
    Even if I still got at this time more shares than them, was the CEO and also the most important guy of this project, the company was their now. And I cannot complain.
    They do their job. They make you sign a ton of papers. And in one of them, it’s usually mentioned that the founder can be fired by the Board members, even if he doesn’t commit any mistakes.
    </p>
    <p class="post">
    But I made mistakes. Two mistakes actually.
    </p>
    <p class="post">
    The first one was to show them how our developers and designers were really working. These guys made a wonderful job, and some of them were the most brilliant I ever met.<br>
    I trust them very much and was doing my best to help them in their tasks. They were moving fast, with agility, and expertise. But without a 6 months plan. Because a startup which knows what would happen in the next 6 months is not a startup anymore, but a company. And I was completely OK with that.<br>
    But my shareholders didn’t approve our agility. They start to became a bit scared.
    </p>
    <p class="post">
    My second mistake was when I answered « yes, of course » at the following question:<br>
    « Is it true that you go kitesurfing once a week during the office hours ? ».<br>
    And actually, I lied. Because unfortunately, I didn’t have the time to go once a week. So I went once or twice a month. But I answered « yes, of course » because I know if I had time it would have been once a week. And I really needed it. I was working 7/7 since 2 years on this project (without counting my insomnia).<br>
    Oops. Now my shareholders were REALLY SCARED.
    </p>
    <p class="post">
    A couple of weeks later, I was fired, and replaced.
    </p>
